#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*btmp;
	unsigned int	i;

	i = 0;
	btmp = s;
	while (i < n)
	{
		btmp[i] = '\0';
		i++;
	}
}
